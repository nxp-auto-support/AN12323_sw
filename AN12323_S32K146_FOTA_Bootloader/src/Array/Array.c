void Clean_Array ( uint32_t *data_buffer_ptr,uint16_t buffer_size)
{
	uint8_t cntr=0;
	do
	{
		*data_buffer_ptr=0x00000000;
		data_buffer_ptr++;
		cntr++;
	}while(cntr<buffer_size);
}

void Array_Copy (uint32_t *data_buffer1_ptr, uint32_t *data_buffer2_ptr,uint16_t array_size)
{
	uint8_t cntr=0;
	do
	{
		*data_buffer2_ptr=*data_buffer1_ptr;
		data_buffer2_ptr++;
		data_buffer1_ptr++;
		cntr++;
	}while(cntr<array_size);
}

uint8_t Compare_Arrays(uint32_t *data_buffer1_ptr, uint32_t *data_buffer2_ptr,uint16_t array_size)
{
	uint8_t error=0;
	uint8_t cntr=0;

	do
	{
		if(*data_buffer1_ptr != *data_buffer2_ptr)
		{
			error = 1;
			break;
		}
		data_buffer2_ptr++;
		data_buffer1_ptr++;
		cntr++;
	}while(cntr<array_size);
	return error;
}
